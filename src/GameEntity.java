public class GameEntity {
    int healthHB;
    int damageHB;

    public GameEntity(int healthHB, int damageHB) {
        this.healthHB = healthHB;
        this.damageHB = damageHB;
    }

    public int getHeathHB() {
        return healthHB;
    }


    public int getDamageHB() {
        return damageHB;
    }


}